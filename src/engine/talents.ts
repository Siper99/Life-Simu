import { Attributes, Talent } from "./types";

let seq = 0;
function t(name: string, desc: string, attrMods: Partial<Attributes>, tags: string[]): Talent {
  return { id: `t${(seq++).toString().padStart(3, "0")}`, name, desc, attrMods, tags };
}

export type TalentRarity = "legendary" | "rare" | "uncommon" | "common";

export const RARITY_LABEL: Record<TalentRarity, string> = {
  legendary: "传说",
  rare: "稀有",
  uncommon: "优秀",
  common: "普通",
};

// 天赋评分 = 属性修正净值之和（双刃剑的负值会拉低分数，怪癖类通常分数较低）。
// 不做人工标注，新增天赋按分数自动落入对应品级。
export function getTalentPower(talent: Talent): number {
  return Object.values(talent.attrMods).reduce((sum: number, v) => sum + (v ?? 0), 0);
}

export function getTalentRarity(talent: Talent): TalentRarity {
  const power = getTalentPower(talent);
  if (power >= 20) return "legendary";
  if (power >= 15) return "rare";
  if (power >= 10) return "uncommon";
  return "common";
}

